// Build lookup maps for fast searching
export const shortcodeToEmoji = new Map<string, string>()
emojiDatabase.forEach(data => {
  data.shortcodes.forEach(code => {
    shortcodeToEmoji.set(code, data.emoji)
  })
})

// Fuzzy search function for autocomplete
export function searchEmojis(query: string): EmojiData[] {
  if (query.length < 3) return []
  const q = query.toLowerCase()
  return emojiDatabase
    .filter(data => {
      // Exact shortcode match (highest priority)
      if (data.shortcodes.some(code => code.startsWith(q))) return true
      // Keyword fuzzy match
      if (data.keywords.some(kw => kw.includes(q))) return true
      return false
    })
    .slice(0, 8) // Limit to 8 results
}

// Get emojis by category (for emoji picker)
export function getEmojisByCategory(category: string): EmojiData[] {
  return emojiDatabase.filter(data => data.category === category)
}

